class ArithmeticOp {


public static void main(String[] args) {

	int a = 4;
	int b = 2;

	int sum = a + b;
	int sub = a - b;
	int mul = a * b;
	int div = a / b;
	int mod = a % b;

	System.out.println("sum is " + sum);
	System.out.println("sub is " + sub);
	System.out.println("mul is " + mul);
	System.out.println("div is " + div);
	System.out.println("mod is " + mod);


}
}